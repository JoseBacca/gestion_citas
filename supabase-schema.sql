-- Políticas: perfiles
CREATE POLICY "profiles_select_own" ON public.profiles
  FOR SELECT USING (auth.uid() = id OR EXISTS (
    SELECT 1 FROM public.profiles p WHERE p.id = auth.uid() AND p.role_id IN (
      SELECT id FROM public.roles WHERE name IN ('COORDINACION', 'SUPERADMIN')
    )
  ));

CREATE POLICY "profiles_update_own" ON public.profiles
  FOR UPDATE USING (auth.uid() = id);

-- Políticas: citas
CREATE POLICY "appointments_select" ON public.appointments
  FOR SELECT USING (
    user_id = auth.uid() OR
    professional_id = auth.uid() OR
    EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = auth.uid() AND p.role_id IN (
      SELECT id FROM public.roles WHERE name IN ('COORDINACION', 'SUPERADMIN')
    ))
  );

CREATE POLICY "appointments_insert" ON public.appointments
  FOR INSERT WITH CHECK (user_id = auth.uid());

CREATE POLICY "appointments_update" ON public.appointments
  FOR UPDATE USING (
    user_id = auth.uid() OR
    professional_id = auth.uid() OR
    EXISTS (SELECT 1 FROM public.profiles p WHERE p.id = auth.uid() AND p.role_id IN (
      SELECT id FROM public.roles WHERE name IN ('COORDINACION', 'SUPERADMIN')
    ))
  );
