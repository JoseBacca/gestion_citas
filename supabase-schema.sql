-- 1. TABLA DE ROLES
CREATE TABLE if not exists public.roles (
  id bigint generated by default as identity primary key,
  name text not null unique,
  permissions jsonb default '{}'::jsonb,
  created_at timestamptz default now()
);

-- 2. TABLA DE DEPENDENCIAS (Psicología, Enfermería, Trabajo Social)
CREATE TABLE if not exists public.dependencies (
  id bigint generated by default as identity primary key,
  name text not null unique,
  color text default '#39a900',
  created_at timestamptz default now()
);

-- 3. TABLA DE PERFILES (extiende auth.users)
CREATE TABLE if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  document_number text unique,
  email text,
  role_id bigint references public.roles(id),
  dependency_id bigint references public.dependencies(id),
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- 4. TABLA DE CITAS
CREATE TABLE if not exists public.appointments (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  professional_id uuid references public.profiles(id),
  dependency_id bigint references public.dependencies(id),
  scheduled_date date not null,
  scheduled_time time not null,
  status text not null default 'pending' check (status in ('pending','confirmed','completed','cancelled','no_show')),
  reason text,
  notes text,
  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

